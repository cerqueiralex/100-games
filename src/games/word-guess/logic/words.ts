/*
 * Word Guess — hand-authored original word banks.
 *
 * Two kinds of list per word length:
 *  - ANSWERS: common, fair words a secret can be drawn from (hard tier uses
 *    a separate bank of rarer-but-real 5-letter words).
 *  - EXTRA: additional legitimate words (plurals, verb forms, adjectives…)
 *    accepted as guesses but never used as secrets. ALLOWED = ANSWERS ∪ EXTRA.
 *
 * All words are uppercase A–Z, no proper nouns. Answer banks avoid plurals
 * and inflected forms; the EXTRA banks are deliberately generous so real
 * words don't get rejected. `validateWordGuessBank()` (run by
 * `npm run validate`) enforces lengths, charset, duplicates, bank sizes,
 * answers ⊆ allowed, answer/extra overlap and rare↔common disjointness.
 */

function bank(raw: string): string[] {
  return raw.trim().split(/\s+/);
}

