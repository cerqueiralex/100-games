/** Statistically strong opening words (per length) used by the Starter assist. */
export const STARTERS: Record<number, string> = {
  4: 'RATE',
  5: 'RAISE',
  6: 'SAILOR',
  7: 'NOTICES'
};

export const ALLOWED_BY_LEN: Record<number, Set<string>> = {
  4: new Set([...ANSWERS_4, ...EXTRA_4]),
  5: new Set([...ANSWERS_5, ...ANSWERS_5_RARE, ...EXTRA_5]),
  6: new Set([...ANSWERS_6, ...EXTRA_6]),
  7: new Set([...ANSWERS_7, ...EXTRA_7])
};

/* ------------------------------------------------------------- validation */

/** Integrity checks for the banks — returns human-readable error strings. */
export function validateWordGuessBank(): string[] {
  const errors: string[] = [];

  const lists: Array<[name: string, words: string[], len: number]> = [
    ['answers-4', ANSWERS_4, 4],
    ['extra-4', EXTRA_4, 4],
    ['answers-5', ANSWERS_5, 5],
    ['answers-5-rare', ANSWERS_5_RARE, 5],
    ['extra-5', EXTRA_5, 5],
    ['answers-6', ANSWERS_6, 6],
    ['extra-6', EXTRA_6, 6],
    ['answers-7', ANSWERS_7, 7],
    ['extra-7', EXTRA_7, 7]
  ];

  for (const [name, words, len] of lists) {
    const seen = new Set<string>();
    for (const w of words) {
      if (!/^[A-Z]+$/.test(w)) errors.push(`${name}: "${w}" has characters outside A-Z`);
      if (w.length !== len) errors.push(`${name}: "${w}" is not ${len} letters`);
      if (seen.has(w)) errors.push(`${name}: duplicate "${w}"`);
      seen.add(w);
    }
  }

  // answer banks stay non-overlapping with their extras (keeps lists clean),
  // and the rare tier stays disjoint from the common tier
  const overlapChecks: Array<[string, string[], string, string[]]> = [
    ['answers-4', ANSWERS_4, 'extra-4', EXTRA_4],
    ['answers-5', ANSWERS_5, 'extra-5', EXTRA_5],
    ['answers-5-rare', ANSWERS_5_RARE, 'extra-5', EXTRA_5],
    ['answers-5', ANSWERS_5, 'answers-5-rare', ANSWERS_5_RARE],
    ['answers-6', ANSWERS_6, 'extra-6', EXTRA_6],
    ['answers-7', ANSWERS_7, 'extra-7', EXTRA_7]
  ];
  for (const [aName, a, bName, b] of overlapChecks) {
    const bSet = new Set(b);
    for (const w of a) if (bSet.has(w)) errors.push(`"${w}" appears in both ${aName} and ${bName}`);
  }

  // minimum sizes (per the game design)
  const sizes: Array<[string, number, number]> = [
    ['answers-4', ANSWERS_4.length, 120],
    ['answers-5', ANSWERS_5.length, 120],
    ['answers-5-rare', ANSWERS_5_RARE.length, 80],
    ['answers-6', ANSWERS_6.length, 80],
    ['answers-7', ANSWERS_7.length, 80],
    ['allowed-4', ALLOWED_BY_LEN[4].size, 300],
    ['allowed-5', ALLOWED_BY_LEN[5].size, 400],
    ['allowed-6', ALLOWED_BY_LEN[6].size, 200],
    ['allowed-7', ALLOWED_BY_LEN[7].size, 200]
  ];
  for (const [name, actual, min] of sizes) {
    if (actual < min) errors.push(`${name} has ${actual} words, needs at least ${min}`);
  }

  // every answer must be an accepted guess
  const answerBanks: Array<[string, string[], number]> = [
    ['answers-4', ANSWERS_4, 4],
    ['answers-5', ANSWERS_5, 5],
    ['answers-5-rare', ANSWERS_5_RARE, 5],
    ['answers-6', ANSWERS_6, 6],
    ['answers-7', ANSWERS_7, 7]
  ];
  for (const [name, words, len] of answerBanks) {
    for (const w of words) {
      if (!ALLOWED_BY_LEN[len].has(w)) errors.push(`${name}: "${w}" missing from allowed set`);
    }
  }

  // starter words must be legal guesses of the right length
  for (const [len, word] of Object.entries(STARTERS)) {
    const n = Number(len);
    if (word.length !== n) errors.push(`starter for length ${n} ("${word}") has wrong length`);
    if (!ALLOWED_BY_LEN[n]?.has(word)) errors.push(`starter "${word}" is not in the allowed set for length ${n}`);
  }

  return errors;
}
